<h1>A propos de l'OMEP</h1>
<p>En tant que catégorie agricole, les métiers de l’Elevage, de la Pêche et de la Santé animale
constituent un potentiel de développement majeur tant du point de vue de leur poids technique
et stratégique, contribuant à la réalisation des Objectifs du Développement Durable (ODD),
que de leur implication active dans le développement de la sphère économique et sociale des
acteurs dans les chaînes valeur de l'Elevage et de la Pêche</p>
<br>
<p>Cependant, malgré leur engagement pour avoir une intégration harmonieuse des différents
maillons des chaînes de valeur du secteur de l’Elevage et de la Pêche, afin de participer de
manière responsable au développement des pays membres de l’EISMV de Dakar ; les acteurs
intervenant dans ces chaînes de valeur et systèmes de santé animale et santé publique
vétérinaire, restent confrontés à de nombreuses difficultés telles que l’accès à l'éducation, à la
formation professionnelle, à l'emploi décent, et surtout à l'information dont ils ont besoin pour
développer leurs activités professionnelles ou pour promouvoir des programmes de
développement.</p>
<br>
<p>Il se trouve, par ailleurs, que les acteurs eux-mêmes, ainsi que les métiers dans les chaînes
de valeur de l’Elevage et de la Pêche sont mal cartographiés et mal connus des autres
secteurs du développement économique et social.</p>
<br>
<p>En effet, peu d'études approfondies et systématiques ont été réalisées sur l’état des acteurs,
des métiers et qualifications, des entreprises ou des organisations professionnelles et parties
prenantes intervenant dans le secteur de l’Elevage et de la Pêche dans l'espace des pays
francophones d'Afrique de l'Ouest. Les statistiques disponibles sont collectées à différents
niveaux et par plusieurs structures et institutions publiques et privées, en l'absence de toute
coordination et de toute collaboration.</p>
<br>
<p>C'est le cas de certaines institutions qui disposent de bases de données techniques portant
par exemple sur la santé animale (UA-BIRA, OIE, SV, ONG...), les ressources génétiques
animales (UA-BIRA), les effectifs du cheptel et le commerce des animaux et produits animaux
(FAO), les vétérinaires et paravétérinaires (OIE), les informations très complexes sur
l'éducation (enseignement technique et professionnel, enseignement supérieur) ou des 
4
départements et institutions produisant des statistiques dans les Ministères en charge de
l’Elevage. Il faut aussi rappeler qu'il n'existe pas d'observatoire national de l'Elevage dans les
pays et les Communautés économiques régionales. Ce qui conduit à un manque de données
systématiques, fiables et complètes sur les métiers des chaînes de valeur de l'élevage et de
la pêche entravant ainsi la prise de décision fondée sur des preuves.</p>
<br>
<p>Dans les Ministères en charge de l'Elevage et de la Pêche, la principale source d'information
sur l'Elevage constituée les rapports annuels. Ils s'y ajoutent, bien que partielles, quelques
études thématiques sur les chaînes de valeur de l'Elevage et de la Pêche réalisées à l'occasion
de projets.
On note aussi très peu d'études thématiques sur les questions de l'insertion professionnelle,
l'évolution des qualifications dans les chaines de valeur du secteur de l'Elevage et de la Pêche,
l'accès à la formation et à l'information par les acteurs.</p>
<br>
<p>Il n'y a pas non plus d'études spécifiques ciblant des catégories d'entreprises et de secteurs
et une base de données fiables des différents métiers dans les chaînes de valeurs de l'Elevage
et de la Pêche. </p>
<br>
<p>C'est pour répondre à cette problématique majeure qu'il est envisagé, la mise en place
opérationnelle de l'Observatoire des Métiers de l'Elevage et de la Pêche (OMEP) de l'EISMV
de Dakar qui sera un espace de recueils et de traitement d'informations qualitatives et
quantitatives relatives aux chaînes de valeur de l'Elevage et aux systèmes de santé animale
et santé publique dans les pays francophones d'Afrique de l'Ouest. Celui-ci permettra aussi
d'éviter les improvisations et les prises de décisions peu adéquates face aux nombreux défis
auxquels le secteur des productions et santé animales et leurs industries de transformation
sont confrontés</p>

