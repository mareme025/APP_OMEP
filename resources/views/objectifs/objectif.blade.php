<h1>OBJECTIFS</h1>
<p>L’objectif principal de l’Observatoire des Métiers de l’Elevage et de la Pêche est de mettre à
la disposition des utilisateurs publics et privés des statistiques et des données sur le secteur
de l'Elevage, de la Pêche et leurs industries et entreprises, des informations fiables qui
orientent les prises de décision dans les initiatives en faveur des politiques de développement
des chaînes de valeurs Elevage et la Pêche, de l'emploi des jeunes, de la réorientation des
programmes de formation initiale, de l'insertion professionnelle des diplômés de l'EISMV, de
l'élaboration de programmes de formation continues ou complémentaires aux différents
métiers émergents concourant au développement de l'Elevage et aux industries de
transformation.
De façon spécifique, il s’agit de :
<br>
➢ fournir des éléments d’informations et d’analyses prospectives (base de données
publiques avec un répertoire élargi d'indicateurs <br>permettant de rassembler des
informations quantitatives et qualitatives fiables sur les chaînes de valeurs  de l'élevage
et les systèmes de santé animale et de santé publique ) ;<br>
➢ collecter, analyser et exploiter les données relatives aux métiers de l’Elevage et à leur
évolution ;<br>
➢ suivre et anticiper les conséquences des évolutions sur les métiers de l’élevage; <br>
➢ faciliter les travaux de réflexion en matière de Gestion Prévisionnelle des Emplois et
Compétences et de formation ;<br>
➢ organiser et animer des rencontres d’experts sur des thèmes spécifiques en lien avec
la problématique d’évolution des métiers d’élevage et de la pêche. <br>
</p>
