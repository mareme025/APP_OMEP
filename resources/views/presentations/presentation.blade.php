<h1>Presentation</h1>

<p>L’EISMV, dès sa création, a une vocation d’enseignement, de recherche et d'expertise. Elle
dispense un enseignement supérieur vétérinaire adapté aux réalités africaines. L’accent dans
cette formation était mis sur la santé animale, la protection sanitaire et les productions
animales.</p>
<br>
<p>Dans son Plan stratégique à l’horizon 2025, l’EISMV prévoit de restructurer et de renforcer les
capacités de l’Observatoire des métiers de l’Elevage et de la Pêche, afin de pouvoir renforcer
la professionnalisation des enseignements et dynamiser la fonction étude-conseil.
Créé par arrêté N°00044/PCA/EISMV du 23 décembre 1996, suite à une décision de la 25ème
session ordinaire du Conseil d’Administration de l’EISMV tenue à Brazzaville (République du
Congo) en juillet 1996, l’Observatoire des Métiers de l’Elevage et de la Pêche (OMEP) de
l’Ecole Inter-Etats des Sciences et Médecine Vétérinaires (EISMV) de Dakar est un organe
consultatif. </p>
<br>
<p>Il contribue à l’adéquation Formation-Emploi en cartographiant les métiers et qualifications
dans les chaines de valeur de l’Elevage et la Pêche, en mesurant leur évolution et devenir et
en suivant l’évolution de l’insertion professionnelle des diplômés de l’EISMV, afin de mieux
cerner les qualifications et les besoins en formation.</p>
<br>
<p>Structure permanente de veille informationnelle, il s’appuie sur un système de collecte
(indicateurs, enquêtes) et de traitement d’informations qualitatives et quantitatives sur l’état
des ressources animales et halieutiques, l’état des ressources humaines et leurs
organisations, les entreprises et les organismes professionnels du secteur de l’Elevage et de
la Pêche des pays francophones d’Afrique de l’Ouest et du Centre, afin de construire une base
accessible de données statistiques. </p>
<br>
<p>Par ailleurs, il est destiné à mettre à la disposition des entreprises du secteur de l’Elevage de
la Pêche et des organisations professionnelles intervenant dans la production animale, la
commercialisation des produits animaux, la transformation des produits animaux, la formation,
l’encadrement et l’enseignement, la fourniture d’intrants, le transports d’animaux et produits
animaux et dans les institutions de financement ; des statistiques et des données fiables
susceptibles d’orienter les prises de décisions adéquates en faveur des politiques de
développement de l’Elevage et la Pêche, de l’emploi des jeunes et de la réorientation des
programmes de formation en lien avec la problématique d’évolution des métiers et des
compétences.</p>
<br>
<p>Par ses éclairages, il participe à la qualité, à la pertinence et à la diversification des formations
à l’EISMV, mais aussi au développement des compétences et professionnalisation des
acteurs, au renforcement des capacités des entreprises et organisations professionnelles des
chaînes de valeur du secteur de l’Elevage et la Pêche.</p>

